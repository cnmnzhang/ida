"""
bime/curate_data.py

Reads Synthea CSV output and produces the three app-ready CSV files for the
BIME digital-twins demo of the IDA Underdiagnosis Streamlit app:

  bime/data/patients.csv
  bime/data/hb_trajectories.csv
  bime/data/diagnoses.csv

Synthea must be run first — see bime/README_synthea.md.

Usage:
  python bime/curate_data.py
  python bime/curate_data.py --synthea-dir /path/to/synthea/output/csv
  python bime/curate_data.py --seed 123

What this script does:
  1. Filters Synthea patients: female, age 18–65, alive
  2. Extracts longitudinal HB measurements (LOINC 718-7) — ≥3 per patient
  3. Computes personalized reference interval (perRI) per patient:
       setpoint = mean of up to 5 historical HB values
       sigma    = std of those values
       ci       = setpoint ± 1.96 * sigma
       drop     = setpoint − most_recent_hb
  4. Assigns hb_bin, delta_bin, bucket, Priority (mirrors src/helper.py)
  5. Simulates ferritin via log-normal model (Synthea does not generate it)
  6. Maps SNOMED-CT conditions → ICD-10 via embedded crosswalk
  7. Anonymizes patient UUIDs via SHA-256 → 12-char hex
  8. Writes output CSVs with no PII
"""

